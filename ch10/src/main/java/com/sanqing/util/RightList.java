package com.sanqing.util;

import java.util.List;

public class RightList {
    private List hasRight;
    private List hasNotRight;
    private List allRight;

    public List getAllRight() {
        return allRight;
    }

    public void setAllRight(List allRight) {
        this.allRight = allRight;
    }

    public List getHasRight() {
        return hasRight;
    }

    public void setHasRight(List hasRight) {
        this.hasRight = hasRight;
    }

    public List getHasNotRight() {
        return hasNotRight;
    }

    public void setHasNotRight(List hasNotRight) {
        this.hasNotRight = hasNotRight;
    }
}
